// @lc code=start
class Solution {
  public:
    int knightDialer(int n) {
        int mod = 1000000000 + 7;
        int res = 0;
        vector<vector<int>> pad = {{4, 6}, {6, 8}, {7, 9}, {4, 8}, {3, 9, 0}, {}, {1, 7, 0}, {2, 6}, {1, 3}, {2, 4}};
        vector<vector<int>> dp(n, vector<int>(10, 0));
        for (int i = 0; i < 10; i++) {
            dp[0][i] = 1;
        }
        for (int i = 1; i < n; i++) {
            for (int j = 0; j < 10; j++) {
                auto fromWhere = pad[j];
                for (auto const &f : fromWhere) {
                    dp[i][j] = (dp[i][j] + dp[i - 1][f]) % mod;
                }
            }
        }
        for (int i = 0; i < 10; ++i) {
            res = (res + dp[n - 1][i]) % mod;
        }
        return res;
    }
};
